#!/bin/bash

###############################################################################
# 知识图谱 Web 应用重启脚本
###############################################################################

# 颜色定义
CYAN='\033[0;36m'
NC='\033[0m' # No Color

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

echo -e "${CYAN}"
echo "╔════════════════════════════════════════════════════════╗"
echo "║     重启知识图谱 Web 应用服务                          ║"
echo "╚════════════════════════════════════════════════════════╝"
echo -e "${NC}\n"

# 停止服务（保留日志）
bash "$SCRIPT_DIR/stop.sh" --keep-logs

echo
sleep 2

# 启动服务
bash "$SCRIPT_DIR/start.sh"
